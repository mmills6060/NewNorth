function parseEnvCandidates(): string[] {
  return (
    process.env.CRAWL_PROBE_SLUGS?.split(",")
      .map((slug) => slug.trim().toLowerCase())
      .filter(Boolean) ?? []
  )
}

export function getBoardCandidates(): string[] {
  const slugs = new Set<string>([...CORE_BOARD_CANDIDATES, ...parseEnvCandidates()])
  return [...slugs]
}
